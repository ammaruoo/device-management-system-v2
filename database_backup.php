    <script>
        document.addEventListener('alpine:init', () => {
            Alpine.data('backupManager', () => ({
                backups: <?php echo json_encode($backups); ?>,
                dbStats: <?php echo json_encode($dbStats); ?>,
                isLoading: false,
                loadingMessage: '',
                isCreating: false,
                backupOptions: {
                    compress: true,
                    includeData: true
                },

                async createBackup() {
                    this.isCreating = true;
                    this.loadingMessage = 'جاري إنشاء النسخة الاحتياطية...';

                    try {
                        const response = await fetch('database_backup.php?action=create_backup&compress=' + this.backupOptions.compress + '&include_data=' + this.backupOptions.includeData);
                        const result = await response.json();

                        if (result.success) {
                            await Swal.fire({
                                icon: 'success',
                                title: 'تم إنشاء النسخة الاحتياطية',
                                html: `
                                    <div class="text-center">
                                        <p class="mb-2">تم إنشاء النسخة بنجاح</p>
                                        <p class="text-sm text-gray-600">الملف: <strong>${result.file}</strong></p>
                                        <p class="text-sm text-gray-600">الحجم: <strong>${this.formatBytes(result.size)}</strong></p>
                                    </div>
                                `
                            });
                            await this.refreshBackups();
                        } else {
                            throw new Error(result.error || 'حدث خطأ غير معروف');
                        }
                    } catch (error) {
                        await Swal.fire({
                            icon: 'error',
                            title: 'خطأ في إنشاء النسخة',
                            text: error.message
                        });
                    } finally {
                        this.isCreating = false;
                    }
                },

                async restoreBackup(filepath, filename) {
                    const result = await Swal.fire({
                        title: 'تأكيد الاستعادة',
                        html: `
                            <div class="text-center">
                                <p class="mb-4">هل أنت متأكد من استعادة النسخة الاحتياطية؟</p>
                                <p class="text-sm text-red-600 bg-red-50 p-3 rounded-lg">
                                    ⚠️ تحذير: هذا الإجراء سيستبدل جميع البيانات الحالية
                                </p>
                                <p class="text-sm text-gray-600 mt-2">الملف: <strong>${filename}</strong></p>
                            </div>
                        `,
                        icon: 'warning',
                        showCancelButton: true,
                        confirmButtonText: 'نعم، استعادة',
                        cancelButtonText: 'إلغاء',
                        confirmButtonColor: '#d33',
                        cancelButtonColor: '#3085d6'
                    });

                    if (!result.isConfirmed) return;

                    this.isLoading = true;
                    this.loadingMessage = 'جاري استعادة النسخة الاحتياطية...';

                    try {
                        const formData = new FormData();
                        formData.append('filepath', filepath);

                        const response = await fetch('database_backup.php?action=restore_backup', {
                            method: 'POST',
                            body: formData
                        });
                        const data = await response.json();

                        if (data.success) {
                            await Swal.fire({
                                icon: 'success',
                                title: 'تمت الاستعادة بنجاح',
                                text: 'تم استعادة النسخة الاحتياطية بنجاح'
                            });
                            // إعادة تحميل الصفحة
                            window.location.reload();
                        } else {
                            throw new Error(data.error || 'حدث خطأ في الاستعادة');
                        }
                    } catch (error) {
                        await Swal.fire({
                            icon: 'error',
                            title: 'خطأ في الاستعادة',
                            text: error.message
                        });
                    } finally {
                        this.isLoading = false;
                    }
                },

                async deleteBackup(filename) {
                    const result = await Swal.fire({
                        title: 'تأكيد الحذف',
                        text: `هل أنت متأكد من حذف النسخة الاحتياطية "${filename}"؟`,
                        icon: 'warning',
                        showCancelButton: true,
                        confirmButtonText: 'نعم، حذف',
                        cancelButtonText: 'إلغاء',
                        confirmButtonColor: '#d33'
                    });

                    if (!result.isConfirmed) return;

                    try {
                        const formData = new FormData();
                        formData.append('filename', filename);

                        const response = await fetch('database_backup.php?action=delete_backup', {
                            method: 'POST',
                            body: formData
                        });
                        const data = await response.json();

                        if (data.success) {
                            await Swal.fire({
                                icon: 'success',
                                title: 'تم الحذف',
                                text: 'تم حذف النسخة الاحتياطية بنجاح'
                            });
                            await this.refreshBackups();
                        } else {
                            throw new Error(data.error || 'حدث خطأ في الحذف');
                        }
                    } catch (error) {
                        await Swal.fire({
                            icon: 'error',
                            title: 'خطأ في الحذف',
                            text: error.message
                        });
                    }
                },

                async cleanupOldBackups() {
                    const result = await Swal.fire({
                        title: 'تنظيف النسخ القديمة',
                        input: 'number',
                        inputLabel: 'حذف النسخ الأقدم من (أيام):',
                        inputValue: 30,
                        inputAttributes: {
                            min: 1,
                            max: 365
                        },
                        showCancelButton: true,
                        confirmButtonText: 'تنظيف',
                        cancelButtonText: 'إلغاء'
                    });

                    if (!result.isConfirmed) return;

                    try {
                        const formData = new FormData();
                        formData.append('days', result.value);

                        const response = await fetch('database_backup.php?action=cleanup', {
                            method: 'POST',
                            body: formData
                        });
                        const data = await response.json();

                        await Swal.fire({
                            icon: 'success',
                            title: 'تم التنظيف',
                            text: `تم حذف ${data.deleted} نسخة احتياطية قديمة`
                        });
                        await this.refreshBackups();
                    } catch (error) {
                        await Swal.fire({
                            icon: 'error',
                            title: 'خطأ في التنظيف',
                            text: error.message
                        });
                    }
                },

                async refreshBackups() {
                    const response = await fetch('database_backup.php?action=get_stats');
                    const stats = await response.json();
                    if (stats.success) {
                        this.dbStats = stats;
                    }

                    // إعادة تحميل قائمة النسخ
                    const backupResponse = await fetch(window.location.href);
                    const html = await backupResponse.text();
                    // استخراج البيانات من HTML (يمكن تحسين هذا)
                    window.location.reload();
                },

                downloadBackup(filename) {
                    const link = document.createElement('a');
                    link.href = `backups/${filename}`;
                    link.download = filename;
                    document.body.appendChild(link);
                    link.click();
                    document.body.removeChild(link);
                },

                formatBytes(bytes) {
                    if (bytes === 0) return '0 B';
                    const k = 1024;
                    const sizes = ['B', 'KB', 'MB', 'GB'];
                    const i = Math.floor(Math.log(bytes) / Math.log(k));
                    return parseFloat((bytes / Math.pow(k, i)).toFixed(2)) + ' ' + sizes[i];
                }
            }));
        });
