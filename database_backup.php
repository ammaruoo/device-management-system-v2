class DatabaseBackup
{
    private $pdo;
    private $backupDir;

    public function __construct($pdo)
    {
        $this->pdo = $pdo;
        $this->backupDir = __DIR__ . '/backups/';

        // إنشاء مجلد النسخ الاحتياطية إذا لم يكن موجوداً
        if (!is_dir($this->backupDir)) {
            mkdir($this->backupDir, 0755, true);
        }
    }

    /**
     * إنشاء نسخة احتياطية
     */
    public function createBackup($compress = true, $includeData = true)
    {
        $timestamp = date('Y-m-d_H-i-s');
        $filename = "backup_{$timestamp}.sql";
        $filepath = $this->backupDir . $filename;

        try {
            // الحصول على قائمة الجداول
            $tables = $this->pdo->query("SHOW TABLES")->fetchAll(PDO::FETCH_COLUMN);

            $sql = "-- Database Backup Created: " . date('Y-m-d H:i:s') . "\n";
            $sql .= "-- Generated by Device Management System\n\n";
            $sql .= "SET SQL_MODE = \"NO_AUTO_VALUE_ON_ZERO\";\n";
            $sql .= "START TRANSACTION;\n\n";

            foreach ($tables as $table) {
                // هيكل الجدول
                $sql .= "-- Table structure for `$table`\n";
                $createTable = $this->pdo->query("SHOW CREATE TABLE `$table`")->fetch(PDO::FETCH_ASSOC);
                $sql .= $createTable['Create Table'] . ";\n\n";

                if ($includeData) {
                    // بيانات الجدول
                    $rows = $this->pdo->query("SELECT * FROM `$table`")->fetchAll(PDO::FETCH_ASSOC);

                    if (!empty($rows)) {
                        $sql .= "-- Data for `$table`\n";
                        foreach ($rows as $row) {
                            $values = array_map([$this, 'escapeValue'], array_values($row));
                            $sql .= "INSERT INTO `$table` VALUES (" . implode(', ', $values) . ");\n";
                        }
                        $sql .= "\n";
                    }
                }
            }

            $sql .= "COMMIT;\n";

            // كتابة الملف
            if ($compress) {
                $gzFilepath = $filepath . '.gz';
                $gz = gzopen($gzFilepath, 'w9');
                gzwrite($gz, $sql);
                gzclose($gz);
                return [
                    'success' => true,
                    'file' => basename($gzFilepath),
                    'size' => filesize($gzFilepath),
                    'path' => $gzFilepath
                ];
            } else {
                file_put_contents($filepath, $sql);
                return [
                    'success' => true,
                    'file' => $filename,
                    'size' => filesize($filepath),
                    'path' => $filepath
                ];
            }

        } catch (Exception $e) {
            return [
                'success' => false,
                'error' => $e->getMessage()
            ];
        }
    }

    /**
     * استعادة نسخة احتياطية
     */
    public function restoreBackup($filepath)
    {
        try {
            // قراءة محتوى الملف
            if (pathinfo($filepath, PATHINFO_EXTENSION) === 'gz') {
                $content = gzread(gzopen($filepath, 'r'), filesize($filepath));
            } else {
                $content = file_get_contents($filepath);
            }

            // تنفيذ الاستعلامات
            $this->pdo->exec($content);

            return [
                'success' => true,
                'message' => 'تم استعادة النسخة الاحتياطية بنجاح'
            ];

        } catch (Exception $e) {
            return [
                'success' => false,
                'error' => 'خطأ في استعادة النسخة: ' . $e->getMessage()
            ];
        }
    }

    /**
     * جلب قائمة النسخ الاحتياطية
     */
    public function getBackupList()
    {
        $files = glob($this->backupDir . '*.{sql,sql.gz}', GLOB_BRACE);

        $backups = [];
        foreach ($files as $file) {
            $filename = basename($file);
            $backups[] = [
                'filename' => $filename,
                'filepath' => $file,
                'size' => filesize($file),
                'created' => filemtime($file),
                'formatted_size' => $this->formatBytes(filesize($file)),
                'formatted_date' => date('Y-m-d H:i:s', filemtime($file)),
                'is_compressed' => pathinfo($file, PATHINFO_EXTENSION) === 'gz'
            ];
        }

        // ترتيب حسب التاريخ (الأحدث أولاً)
        usort($backups, function($a, $b) {
            return $b['created'] - $a['created'];
        });

        return $backups;
    }

    /**
     * حذف نسخة احتياطية
     */
    public function deleteBackup($filename)
    {
        $filepath = $this->backupDir . $filename;

        if (file_exists($filepath)) {
            unlink($filepath);
            return ['success' => true];
        }

        return ['success' => false, 'error' => 'الملف غير موجود'];
    }

    /**
     * تنظيف النسخ القديمة
     */
    public function cleanupOldBackups($days = 30)
    {
        $files = glob($this->backupDir . '*.{sql,sql.gz}', GLOB_BRACE);
        $deleted = 0;

        foreach ($files as $file) {
            if (filemtime($file) < time() - ($days * 24 * 60 * 60)) {
                unlink($file);
                $deleted++;
            }
        }

        return ['deleted' => $deleted];
    }

    /**
     * جلب إحصائيات قاعدة البيانات
     */
    public function getDatabaseStats()
    {
        try {
            // عدد الجداول
            $tables = $this->pdo->query("SHOW TABLES")->fetchAll(PDO::FETCH_COLUMN);
            $tableCount = count($tables);

            // حجم قاعدة البيانات
            $dbSize = $this->pdo->query("
                SELECT ROUND(SUM(data_length + index_length) / 1024 / 1024, 2) as size_mb
                FROM information_schema.TABLES
                WHERE table_schema = DATABASE()
            ")->fetch(PDO::FETCH_ASSOC);

            // عدد السجلات في كل جدول
            $tableStats = [];
            foreach ($tables as $table) {
                $count = $this->pdo->query("SELECT COUNT(*) FROM `$table`")->fetchColumn();
                $tableStats[$table] = $count;
            }

            return [
                'success' => true,
                'table_count' => $tableCount,
                'database_size' => $dbSize['size_mb'] ?? 0,
                'table_stats' => $tableStats,
                'total_records' => array_sum($tableStats)
            ];

        } catch (Exception $e) {
            return [
                'success' => false,
                'error' => $e->getMessage()
            ];
        }
    }

    /**
     * دوال مساعدة
     */
    private function escapeValue($value)
    {
        if ($value === null) {
            return 'NULL';
        }

        if (is_numeric($value)) {
            return $value;
        }

        return $this->pdo->quote($value);
    }

    private function formatBytes($bytes)
    {
        $units = ['B', 'KB', 'MB', 'GB'];
        $i = 0;

        while ($bytes >= 1024 && $i < count($units) - 1) {
            $bytes /= 1024;
            $i++;
        }

        return round($bytes, 2) . ' ' . $units[$i];
    }
}

